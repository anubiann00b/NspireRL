int main(void)
{
	int playerX=2, playerY=10;
    nio_console csl;
	init(csl);
	drawMap();
	writeCharG(playerX,playerY,'@');
	while(!isKeyPressed(KEY_NSPIRE_ESC))
	{
		int dx=0, dy=0;
		if (isKeyPressed(KEY_NSPIRE_8) || isKeyPressed(KEY_NSPIRE_UP))
		{
			dy=-1;
		}
		if (isKeyPressed(KEY_NSPIRE_2) || isKeyPressed(KEY_NSPIRE_DOWN))
		{
			dy=1;
		}
		if (isKeyPressed(KEY_NSPIRE_4) || isKeyPressed(KEY_NSPIRE_LEFT))
		{
			dx=-1;
		}
		if (isKeyPressed(KEY_NSPIRE_6) || isKeyPressed(KEY_NSPIRE_RIGHT))
		{
			dx=1;
		}
		if(isPassable(playerX+dx,playerY+dy))
        {
			playerX+=dx;
			playerY+=dy;
		}
		if (dx!=0 || dy!=0)
		{
			writeCharG(playerX,playerY,'@');
			writeCharG(playerX-dx,playerY-dy,getMapTile(playerX-dx,playerY-dy));
		}
	}
    cleanUp(csl);
	return 0;
}

int isPassable(int x, int y)
{
    if(x<0 || x>=MAP_WIDTH || y<0 || y>=MAP_HEIGHT)
        return 0;
	int tileValue = mapArray[y][x];
    if(tileValue == TILE_FLOOR)
        return 1;
    return 0;
}

char getMapTile(int x, int y)
{
	if(mapArray[y][x]==TILE_FLOOR)
	{
		return '.';
	}
	else if(mapArray[y][x]==TILE_WALL)
	{
		return '#';
	}
	else
	{
		return 'x';
	}
}

void drawMap(void)
{
	int x,y;
    for(y=0;y<MAP_HEIGHT;y++)
    {
        for (x=0;x<MAP_WIDTH;x++)
        {
			writeCharG(x,y,getMapTile(x,y));
        }
    }
}

int median(int A, int B, int C)
{
    if ((A-B)*(C-A)>=0)
	{
        return A;
    }
	else if ((B-A)*(C-B)>=0)
	{
        return B;
    }
	else
	{
        return C;
    }
}

void writeCharC(int x, int y, char ch, int color)
{
	putChar(x*6,y*8,ch,0,color);
}

void writeCharG(int x, int y, char ch)
{
	putChar(x*6,y*8,ch,0,15);
}

void writeStrC(int x, int y, char* str, int color)
{
	putStr(x*6,y*8,str,0,color);
}

void writeStrG(int x, int y, char* str)
{
	putStr(x*6,y*8,str,0,15);
}
